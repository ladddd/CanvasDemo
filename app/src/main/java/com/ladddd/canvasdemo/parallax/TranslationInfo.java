public class TranslationInfo {

    private int recyclerViewHeight;
    private int recyclerViewTop;
    private int itemTop;

    public int getRecyclerViewHeight() {
        return recyclerViewHeight;
    }

    public void setRecyclerViewHeight(int recyclerViewHeight) {
        this.recyclerViewHeight = recyclerViewHeight;
    }

    public int getRecyclerViewTop() {
        return recyclerViewTop;
    }

    public void setRecyclerViewTop(int recyclerViewTop) {
        this.recyclerViewTop = recyclerViewTop;
    }

    public int getItemTop() {
        return itemTop;
    }

    public void setItemTop(int itemTop) {
        this.itemTop = itemTop;
    }
}
